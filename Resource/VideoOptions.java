package Resource;

/**
 * The default render resolution height and width are stored in this interface.
 */
public interface VideoOptions
{

    /**
     * Vertical Resolution at which the Game is being rendered and then scaled
     * down from
     *
     * Default Resolution: 4K 16:9.
     */
    public final static int DEFAULT_RENDER_RESOLUTION_HEIGHT = 2160;

    /**
     * Horizontal Resolution at which the Game is being rendered and then scaled
     * down from
     *
     * Default Resolution: 4K 16:9.
     */
    public final static int DEFAULT_RENDER_RESOLUTION_WIDTH = 3840;

}
